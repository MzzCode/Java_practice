package com.Java2025.loops;

// same as while loop the difference is this it execute at least one time 
// even condition is true or not
public class DoWhileMain {
	
	public static void main(String args[]) {
		int i =1; // initial condition
		do {
			System.out.print(i + " ");
			i++; // update statement
		}
		while(i>=10); // condition
		
		//even condition does not satisfied it runs one time 
		
	}

}
